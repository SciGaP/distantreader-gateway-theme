			<h2 class="text-center">
				How to use the Distant Reader
			</h2>
			<hr class="separation-line blue-bg" />
		</div>
		<p style="font-size:18px; margin-top:20px;" align="left">
			There are more than a few steps for to getting the Distant Reader to work for you:
		</p>
		<ol style="font-size:18px; margin-top:20px;" align="left">
			<li>Articulate a research question: What do you want to know?</li>
			<li>Identify one of three different types of input pointing to the content you want to "read": 1) a URL, 2) a file of URLS, or 3) just about any set of files.</li>
			<li>If you haven't already done so <a href="/create">create an account</a>.</li>
			<li><a href="/login">Log in</a>.</li>
			<li><a href="/experiment/create">Submit your input</a>.</li>
			<li><a href="/experiment/browse">Wait</a>.</li>
			<li><a href="/experiment/browse">Download</a> the resulting Distant Reader "study carrel".</li>
			<li>Use the "study carrel" to supplement your usual reading process.</li>
		</ol>
		<p style="font-size:18px; margin-top:20px;" align="left">
		There is a lot of information there, and believe me, the process is faster the second time around.</p>

			<hr class="separation-line blue-bg" />

		<h3>Articulate a research question</h3>
		<p style="font-size:18px; margin-top:20px;" align="left">
			Ask yourself, "What do I want to know?"</p>
		<p style="font-size:18px; margin-top:20px;" align="left">
			The answer to this question can range from the simple to the sublime, for example:</p>
			<ol style="font-size:18px; margin-top:20px;" align="left">
			<li>How many documents are in my corpus?</li>
			<li>How long are my documents?</li>
			<li>On a scale between 0 and 100, how difficult are my documents read?</li>
			<li>What are some of the most statistically significant keywords in my corpus?</li>
			</o>
		<h3>Identify your input</h3>
		<p style="font-size:18px; margin-top:20px;" align="left">
		The input can be any one of the following three things: a URL, a file of URLs, or just about any set of files on your computer.
		</p>
		<h3>Create an account</h3>
		<p style="font-size:18px; margin-top:20px;" align="left">
		If you haven't already done so, <a href="/create">create an account</a>.</p>
		
		<p style="font-size:18px; margin-top:20px;" align="left">The Distant Reader process creates its "study carrels" for you, and consequently the Reader needs to know for whom it is doing its processing. Your employer may already be affiliated with the Reader's authentication process, and if so, you will not need to create an account. Otherwise, you will need to tell us a bit about yourself. The process is harmless. We promise. 
		</p>
		<h3>Log in</h3>
		<p style="font-size:18px; margin-top:20px;" align="left">
		<a href="/login">Authenticate</a> yourself; let the Distant Reader know for whom it is doing its good work.
		</p>
		<h3>Submit your input</h3>
		<p style="font-size:18px; margin-top:20px;" align="left">
		Share your input with the Distant Reader so it can do its good work.
		</p>
		<h3>Wait</h3>
		<p style="font-size:18px; margin-top:20px;" align="left">
		Be patient. The Distant Reader is designed to handle hundreds of URLs pointing to 100's of megabytes of files. Depending on the size of your input, processing can take anywhere between two minutes and a few hours, with a "usual" wait time of ten to fifteen minutes.
		</p>
		<h3>Download your "study carrel"</h3>
		<p style="font-size:18px; margin-top:20px;" align="left">
		The results of the Distant Reader process is a "study carrel" -- a .zip file containing your original content, various transformations of it, and various sets of analysis.</p>
		<h3>Read</h3>
		<p style="font-size:18px; margin-top:20px;" align="left">
		Use the contents of the "study carrel" to suppliment  your usual reading process.
		</p>
		
